public class _10 {
    public static void main(String[] args) {

        int x = 15;
        int y = 10;

        // swap elements without using another variable.
        System.out.println(x);
        System.out.println(y);

        x = x + y;
        y = x - y;
        x = x - y;

        System.out.println("____________________________");
        System.out.println(x);
        System.out.println(y);
    }
}